package com.puresoftware.model;

public class ARC {

	String arcLocation;
	int pincode;
	String claimantLocation;

	public String getArcLocation() {
		return arcLocation;
	}

	public void setArcLocation(String arcLocation) {
		this.arcLocation = arcLocation;
	}

	public int getPincode() {
		return pincode;
	}

	public void setPincode(int pincode) {
		this.pincode = pincode;
	}

	public String getClaimantLocation() {
		return claimantLocation;
	}

	public void setClaimantLocation(String claimantLocation) {
		this.claimantLocation = claimantLocation;
	}

}
